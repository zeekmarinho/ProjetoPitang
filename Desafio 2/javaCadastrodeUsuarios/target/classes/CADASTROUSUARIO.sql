CREATE TABLE USUARIO 
(
  NOME VARCHAR2(200) NOT NULL 
, CPF VARCHAR2(11) NOT NULL 
, EMAIL VARCHAR2(200) NOT NULL
, SENHA VARCHAR2(8) NOT NULL
, CONSTRAINT USUARIO_PK PRIMARY KEY 
  (
    CPF 
  )
  ENABLE 
);

CREATE TABLE TELEFONES 
(
  ID NUMBER NOT NULL 
, DDD VARCHAR2(3) 
, NUMERO VARCHAR2(20)
, TIPO VARCHAR2(200)
, CPF_USUARIO VARCHAR2(11) NOT NULL 
, CONSTRAINT TELEFONES_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

